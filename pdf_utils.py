from fpdf import FPDF

def generate_pdf(entry):
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Helvetica", "B", 16)
    pdf.set_x(pdf.l_margin)
    pdf.multi_cell(0, 10, "Health Triage Report")
    pdf.set_font("Helvetica", "", 11)
    pdf.set_x(pdf.l_margin)
    pdf.multi_cell(0, 8, f"Generated: {entry['timestamp']}")
    pdf.ln(3)
    pdf.set_x(pdf.l_margin)
    pdf.multi_cell(0, 8, f"Symptoms reported: {', '.join(entry['symptoms'])}")
    pdf.ln(2)
    pdf.set_x(pdf.l_margin)
    pdf.multi_cell(0, 8, f"Severity: {entry['severity']}")
    if entry.get("condition"):
        pdf.set_x(pdf.l_margin)
        pdf.multi_cell(0, 8, f"Predicted condition: {entry['condition']} ({entry.get('confidence', 0)*100:.0f}% confidence)")
    pdf.ln(5)
    pdf.set_font("Helvetica", "I", 9)
    pdf.set_x(pdf.l_margin)
    pdf.multi_cell(0, 8, "Disclaimer: This report is generated by an AI triage assistant and is not a medical diagnosis. Please consult a qualified healthcare professional.")
    return bytes(pdf.output())
